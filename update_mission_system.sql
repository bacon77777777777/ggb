-- 1. Create Daily Check-in Table
CREATE TABLE IF NOT EXISTS daily_check_ins (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    check_in_date DATE NOT NULL DEFAULT CURRENT_DATE,
    created_at TIMESTAMPTZ DEFAULT NOW(),
    reward_amount INTEGER DEFAULT 10,
    CONSTRAINT unique_daily_check_in UNIQUE (user_id, check_in_date)
);

ALTER TABLE daily_check_ins ENABLE ROW LEVEL SECURITY;

DROP POLICY IF EXISTS "Users can view own check-ins" ON daily_check_ins;
CREATE POLICY "Users can view own check-ins" ON daily_check_ins FOR SELECT USING (auth.uid() = user_id);

-- 2. Function: Get Check-in Status
CREATE OR REPLACE FUNCTION get_check_in_status(p_user_id UUID)
RETURNS JSONB AS $$
DECLARE
    v_streak INTEGER := 0;
    v_checked_in_today BOOLEAN := false;
    v_today DATE := CURRENT_DATE;
    v_check_date DATE;
BEGIN
    -- Check today
    SELECT EXISTS(SELECT 1 FROM daily_check_ins WHERE user_id = p_user_id AND check_in_date = v_today)
    INTO v_checked_in_today;

    -- Calculate streak
    v_check_date := v_today;
    IF NOT v_checked_in_today THEN
        v_check_date := v_today - 1;
    END IF;

    LOOP
        PERFORM 1 FROM daily_check_ins WHERE user_id = p_user_id AND check_in_date = v_check_date;
        IF FOUND THEN
            v_streak := v_streak + 1;
            v_check_date := v_check_date - 1;
        ELSE
            EXIT;
        END IF;
    END LOOP;

    RETURN jsonb_build_object(
        'consecutive_days', v_streak,
        'checked_in_today', v_checked_in_today,
        'next_reward', 10 + (v_streak % 7) * 5
    );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- 3. Function: Daily Check-in
CREATE OR REPLACE FUNCTION daily_check_in(p_user_id UUID)
RETURNS JSONB AS $$
DECLARE
    v_streak INTEGER;
    v_reward INTEGER;
    v_today DATE := CURRENT_DATE;
BEGIN
    IF EXISTS(SELECT 1 FROM daily_check_ins WHERE user_id = p_user_id AND check_in_date = v_today) THEN
        RETURN jsonb_build_object('success', false, 'message', '今日已簽到');
    END IF;

    -- Calculate streak (from yesterday)
    DECLARE
        v_check_date DATE := v_today - 1;
        v_temp_streak INTEGER := 0;
    BEGIN
        LOOP
            PERFORM 1 FROM daily_check_ins WHERE user_id = p_user_id AND check_in_date = v_check_date;
            IF FOUND THEN
                v_temp_streak := v_temp_streak + 1;
                v_check_date := v_check_date - 1;
            ELSE
                EXIT;
            END IF;
        END LOOP;
        v_streak := v_temp_streak;
    END;

    v_reward := 10 + (v_streak % 7) * 5;

    INSERT INTO daily_check_ins (user_id, check_in_date, reward_amount)
    VALUES (p_user_id, v_today, v_reward);

    -- Update users tokens
    UPDATE users SET tokens = COALESCE(tokens, 0) + v_reward WHERE id = p_user_id;

    RETURN jsonb_build_object(
        'success', true, 
        'reward', v_reward,
        'streak', v_streak + 1
    );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

