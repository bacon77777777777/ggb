CREATE OR REPLACE FUNCTION daily_check_in(p_user_id UUID)
RETURNS JSONB AS $$
DECLARE
  v_today DATE := CURRENT_DATE;
  v_task RECORD;
  v_week TEXT := to_char(NOW(), 'IYYY-IW');
BEGIN
  -- Insert check-in record
  INSERT INTO daily_check_ins (user_id, check_in_date) VALUES (p_user_id, v_today)
  ON CONFLICT DO NOTHING;
  
  IF NOT FOUND THEN
     RETURN jsonb_build_object('success', false, 'message', 'Already checked in today');
  END IF;

  -- Update Daily Task
  SELECT * INTO v_task FROM tasks WHERE condition_type = 'login' AND type = 'daily' AND is_active = true LIMIT 1;
  IF FOUND THEN
    INSERT INTO user_task_progress (user_id, task_id, progress, period_key)
    VALUES (p_user_id, v_task.id, 1, to_char(v_today, 'YYYY-MM-DD'))
    ON CONFLICT (user_id, task_id, period_key) DO UPDATE SET progress = 1, last_updated = NOW();
    
    UPDATE user_task_progress SET is_completed = true 
    WHERE user_id = p_user_id AND task_id = v_task.id AND period_key = to_char(v_today, 'YYYY-MM-DD');
  END IF;

  -- Update Weekly Task (Count check-ins this week)
  DECLARE
    v_weekly_count INTEGER;
  BEGIN
    SELECT COUNT(*) INTO v_weekly_count FROM daily_check_ins 
    WHERE user_id = p_user_id AND check_in_date >= date_trunc('week', v_today)::date;
    
    FOR v_task IN SELECT * FROM tasks WHERE condition_type = 'login' AND type = 'weekly' AND is_active = true LOOP
      INSERT INTO user_task_progress (user_id, task_id, progress, period_key)
      VALUES (p_user_id, v_task.id, v_weekly_count, v_week)
      ON CONFLICT (user_id, task_id, period_key) DO UPDATE SET progress = v_weekly_count, last_updated = NOW();
      
      UPDATE user_task_progress SET is_completed = true 
      WHERE user_id = p_user_id AND task_id = v_task.id AND period_key = v_week AND progress >= v_task.target_value AND is_completed = false;
    END LOOP;
  END;

  RETURN jsonb_build_object('success', true);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

