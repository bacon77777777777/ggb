              VALUES (NEW.user_id, v_task.id, v_price, v_period_key)
              ON CONFLICT (user_id, task_id, period_key) 
              DO UPDATE SET progress = user_task_progress.progress + v_price, last_updated = NOW();
           END IF;
        END;
    END IF;

    -- Condition: play_unique_machine
    IF v_task.condition_type = 'play_unique_machine' THEN
        -- This requires tracking unique product_ids played this week.
        -- We can store the list in metadata JSONB
        DECLARE
           v_meta JSONB;
           v_pid TEXT := NEW.product_id::text;
           v_current_progress INT;
        BEGIN
           SELECT progress, metadata INTO v_current_progress, v_meta FROM user_task_progress 
           WHERE user_id = NEW.user_id AND task_id = v_task.id AND period_key = v_period_key;
           
           IF NOT FOUND THEN
             v_current_progress := 0;
             v_meta := '{"played_ids": []}'::jsonb;
           END IF;
           
           IF NOT (v_meta->'played_ids' ? v_pid) THEN
              v_meta := jsonb_set(v_meta, '{played_ids}', (v_meta->'played_ids') || to_jsonb(v_pid));
              INSERT INTO user_task_progress (user_id, task_id, progress, period_key, metadata)
              VALUES (NEW.user_id, v_task.id, 1, v_period_key, v_meta)
              ON CONFLICT (user_id, task_id, period_key) 
              DO UPDATE SET progress = jsonb_array_length(v_meta->'played_ids'), metadata = v_meta, last_updated = NOW();
           END IF;
        END;
    END IF;

    -- Condition: win_sr
    IF v_task.condition_type = 'win_sr' AND v_is_sr_up THEN
       INSERT INTO user_task_progress (user_id, task_id, progress, period_key)
       VALUES (NEW.user_id, v_task.id, 1, v_period_key)
       ON CONFLICT (user_id, task_id, period_key) 
       DO UPDATE SET progress = user_task_progress.progress + 1, last_updated = NOW();
    END IF;

    -- Check completion
    UPDATE user_task_progress 
    SET is_completed = true 
    WHERE user_id = NEW.user_id 
      AND task_id = v_task.id 
      AND period_key = v_period_key 
      AND progress >= v_task.target_value 
      AND is_completed = false;
      
  END LOOP;
  
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

