        'amount', p_amount,
        'bonus', p_bonus,
        'added_tokens', v_total_tokens,
        'new_balance', v_new_balance
      )
    );
  END IF;

  RETURN json_build_object(
    'success', true,
    'order_number', v_order_number,
    'added_tokens', v_total_tokens,
    'new_balance', v_new_balance
  );
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

