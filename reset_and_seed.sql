-- 10. RPC: Track Mission Event (Frontend triggers)
CREATE OR REPLACE FUNCTION track_mission_event(p_event_type TEXT, p_data JSONB DEFAULT '{}'::jsonb)
RETURNS JSONB AS $$
DECLARE
  v_user_id UUID;
  v_task RECORD;
  v_period_key TEXT;
  v_current_progress INT;
  v_meta JSONB;
  v_item_id TEXT;
BEGIN
  v_user_id := auth.uid();
  IF v_user_id IS NULL THEN
    RETURN jsonb_build_object('success', false, 'message', 'Not authenticated');
  END IF;

  FOR v_task IN SELECT * FROM tasks WHERE condition_type = p_event_type AND is_active = true LOOP
    IF v_task.type = 'daily' THEN
      v_period_key := to_char(NOW(), 'YYYY-MM-DD');
    ELSIF v_task.type = 'weekly' THEN
      v_period_key := to_char(NOW(), 'IYYY-IW');
    ELSE
      v_period_key := 'ALL';
    END IF;

    IF p_event_type = 'view_product' THEN
      v_item_id := p_data->>'product_id';
      SELECT progress, metadata INTO v_current_progress, v_meta FROM user_task_progress 
      WHERE user_id = v_user_id AND task_id = v_task.id AND period_key = v_period_key;
      
      IF NOT FOUND THEN
        v_current_progress := 0;
        v_meta := '{"viewed_ids": []}'::jsonb;
      END IF;
      
      IF v_meta->'viewed_ids' ? v_item_id THEN
        CONTINUE; 
      END IF;
      
      v_meta := jsonb_set(v_meta, '{viewed_ids}', (v_meta->'viewed_ids') || to_jsonb(v_item_id));
      
      INSERT INTO user_task_progress (user_id, task_id, progress, period_key, metadata)
      VALUES (v_user_id, v_task.id, 1, v_period_key, v_meta)
      ON CONFLICT (user_id, task_id, period_key) 
      DO UPDATE SET progress = jsonb_array_length(v_meta->'viewed_ids'), metadata = v_meta, last_updated = NOW();
      
    ELSIF p_event_type = 'share_app' OR p_event_type = 'like_ranking' THEN
      INSERT INTO user_task_progress (user_id, task_id, progress, period_key)
      VALUES (v_user_id, v_task.id, 1, v_period_key)
      ON CONFLICT (user_id, task_id, period_key) 
      DO UPDATE SET progress = user_task_progress.progress + 1, last_updated = NOW();
    END IF;
    
    -- Check completion
    UPDATE user_task_progress 
    SET is_completed = true 
    WHERE user_id = v_user_id 
      AND task_id = v_task.id 
      AND period_key = v_period_key 
      AND progress >= v_task.target_value 
      AND is_completed = false;
      
  END LOOP;

  RETURN jsonb_build_object('success', true);
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

-- 11. Get User Missions Status (Frontend fetch)
DROP FUNCTION IF EXISTS get_user_missions();
CREATE OR REPLACE FUNCTION get_user_missions()
RETURNS TABLE (
  id UUID,
  type TEXT,
  title TEXT,
  description TEXT,
  target_value INTEGER,
  reward_coins INTEGER,
  condition_type TEXT,
  icon_name TEXT,
  progress INTEGER,
  is_completed BOOLEAN,
  is_claimed BOOLEAN,
  period_key TEXT
) AS $$
DECLARE
  v_user_id UUID;
  v_today TEXT := to_char(NOW(), 'YYYY-MM-DD');
  v_week TEXT := to_char(NOW(), 'IYYY-IW');
BEGIN
  v_user_id := auth.uid();
  
  RETURN QUERY
  SELECT 
    t.id,
    t.type,
    t.title,
    t.description,
    t.target_value,
    t.reward_coins,
    t.condition_type,
    t.icon_name,
    COALESCE(utp.progress, 0) as progress,
    COALESCE(utp.is_completed, false) as is_completed,
    COALESCE(utp.is_claimed, false) as is_claimed,
    COALESCE(utp.period_key, 
      CASE 
        WHEN t.type = 'daily' THEN v_today
        WHEN t.type = 'weekly' THEN v_week
        ELSE 'ALL'
      END
    ) as period_key
  FROM tasks t
  LEFT JOIN user_task_progress utp ON t.id = utp.task_id AND utp.user_id = v_user_id 
    AND (
      (t.type = 'daily' AND utp.period_key = v_today) OR
      (t.type = 'weekly' AND utp.period_key = v_week) OR
      (t.type = 'achievement' AND utp.period_key = 'ALL')
    )
  WHERE t.is_active = true
  ORDER BY 
    CASE WHEN COALESCE(utp.is_claimed, false) THEN 2 ELSE 0 END,
    CASE WHEN COALESCE(utp.is_completed, false) THEN 0 ELSE 1 END,
    t.type,
    t.reward_coins DESC;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

